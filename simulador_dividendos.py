def apurar_irpf_normal_anual(df_rends, deducoes=0):
    """
    Apura IRPF "normal" do ajuste anual
    Retorna: (ir_devido_anual, irrf_normal, saldo_irpf_normal)
    """
    # Soma rendas tributáveis no ajuste (tribut avel_ajuste == 1)
    base_ajuste = 0.0
    irrf_normal = 0.0

    for _, renda in df_rends.iterrows():
        tipo = renda.get("tipo", "")
        valor = renda.get("valor_anual", 0.0)

        if tipo in TIPOS_RENDA:
            config = TIPOS_RENDA[tipo]
            # Se é tributável no ajuste
            if config["tributavel_ajuste"] == 1:
                base_ajuste += valor

            # IRRF normal (M1 progressivo mensal)
            if config["tabela_id"] == "M1" and config["tributavel_ajuste"] == 1:
                valor_mensal = valor / 12
                irrf_normal += calcular_irrf_tabela_m1(valor_mensal) * 12

    # Aplica deduções
    base_ajuste = max(0, base_ajuste - deducoes)

    # Calcula IR devido anual
    ir_devido_anual = calcular_irpf_anual(base_ajuste)

    # Aplica redução do art. 11-A
    reducao_11a = calcular_reducao_art11A(base_ajuste)
    ir_devido_anual = max(0, ir_devido_anual - reducao_11a)

    # Saldo IRPF normal
    saldo_irpf_normal = ir_devido_anual - irrf_normal

    return ir_devido_anual, irrf_normal, saldo_irpf_normal

def apurar_base_irpfm(df_rends, df_divs):
    """
    Apura base do IRPFM (REND - exceções do §3)
    Exceções: poupança, indenizações, FIIs/Fiagros, LCI/LCA/CRI/CRA isentos
    """
    rend_total = 0.0

    # Soma rendas que contam para REND (conta_REND == 1)
    for _, renda in df_rends.iterrows():
        tipo = renda.get("tipo", "")
        valor = renda.get("valor_anual", 0.0)

        if tipo in TIPOS_RENDA:
            config = TIPOS_RENDA[tipo]
            if config["conta_REND"] == 1:
                rend_total += valor

    # Soma dividendos (sempre contam para REND)
    total_dividendos = df_divs["valor"].sum() if not df_divs.empty else 0.0
    rend_total += total_dividendos

    # Aplica exceções do §3 (base IRPFM)
    base_irpfm = rend_total

    # Remove rendas que não incluem na base (inclui_BASE == 0)
    for _, renda in df_rends.iterrows():
        tipo = renda.get("tipo", "")
        valor = renda.get("valor_anual", 0.0)

        if tipo in TIPOS_RENDA:
            config = TIPOS_RENDA[tipo]
            if config["conta_REND"] == 1 and config["inclui_BASE"] == 0:
                base_irpfm -= valor

    return rend_total, max(0, base_irpfm)

def calcular_aliquota_irpfm(rend_base):
    """
    Calcula alíquota IRPFM (0% a 10%) conforme art. 16-A
    REND ≤ 600.000 → 0%
    600.000 < REND < 1.200.000 → linear
    REND ≥ 1.200.000 → 10%
    """
    if rend_base <= 600000:
        return 0.0
    elif rend_base >= 1200000:
        return 0.10
    else:
        # Linear entre 600k e 1.2M
        return 0.10 * ((rend_base - 600000) / 600000)

def apurar_irpfm(df_rends, df_divs, df_pj, ir_devido_anual, deducoes_extras=0):
    """
    Apura IRPFM (Imposto de Renda Mínimo)
    Retorna: (irpfm_bruto, irpfm_liquido, irpfm_antecipado, saldo_irpfm)
    """
    # Calcula base IRPFM
    rend_total, base_irpfm = apurar_base_irpfm(df_rends, df_divs)

    # Calcula alíquota IRPFM
    aliq_irpfm = calcular_aliquota_irpfm(base_irpfm)

    # IRPFM bruto
    irpfm_bruto = aliq_irpfm * base_irpfm

    # Deduções do §4 do art. 16-A
    deducoes_irpfm = ir_devido_anual

    # (ii) IR exclusivo retido na fonte sobre rendas incluídas na base do IRPFM
    ir_exclusivo_retido = 0.0
    for _, renda in df_rends.iterrows():
        tipo = renda.get("tipo", "")
        ir_exclusivo = renda.get("irrf_exclusivo_fonte", 0.0)

        if tipo in TIPOS_RENDA:
            config = TIPOS_RENDA[tipo]
            if config["inclui_BASE"] == 1:
                ir_exclusivo_retido += ir_exclusivo

    deducoes_irpfm += ir_exclusivo_retido

    # (v) Redutor 16-B (simplificado - pode ser expandido)
    redutor_16b = 0.0
    # TODO: Implementar cálculo completo do redutor 16-B

    deducoes_irpfm += redutor_16b + deducoes_extras

    # IRPFM líquido
    irpfm_liquido = max(0, irpfm_bruto - deducoes_irpfm)

    # IRPFM antecipado (IRRF 10% sobre dividendos)
    irpfm_antecipado = 0.0
    if not df_divs.empty and "irrf_10" in df_divs.columns:
        irpfm_antecipado = df_divs["irrf_10"].sum()

    # Saldo IRPFM
    saldo_irpfm = irpfm_liquido - irpfm_antecipado

    return irpfm_bruto, irpfm_liquido, irpfm_antecipado, saldo_irpfm, aliq_irpfm, base_irpfm, rend_total

def conciliar_final_art13(saldo_irpf_normal, saldo_irpfm):
    """
    Conciliação final (Art. 13): pagar vs. restituir
    Se negativo → restituição
    Se positivo → imposto a pagar
    """
    saldo_final = saldo_irpf_normal + saldo_irpfm

    if saldo_final < 0:
        return "restituir", abs(saldo_final), 0.0
    else:
        return "pagar", 0.0, saldo_final

