def calcular_saldo_isento_2025():
    """Calcula o saldo isento 2025 baseado nas respostas do usuário"""
    # Q1: existe lucro não distribuído apurado até 2025?
    tem_lucro_nd = saldo_tem_lucro_nd.value

    # Q2: a deliberação foi aprovada até 31/12/2025?
    deliberado_2025 = saldo_deliberado_2025.value

    # Se ambas as respostas são "Sim", cria o saldo isento
    if tem_lucro_nd and deliberado_2025:
        saldo_aprovado = parse_brl(saldo_valor_aprovado.value)
        return saldo_aprovado
    else:
        return 0.0

def aplicar_regra_saldo_isento(df_dividendos, saldo_inicial, piso_mensal, irrf_frac):
    """
    Aplica a regra do saldo isento 2025 aos dividendos

    Args:
        df_dividendos: DataFrame com os dividendos
        saldo_inicial: Saldo isento disponível
        piso_mensal: Piso mensal para IRRF 10%
        irrf_frac: Fração do IRRF (ex: 0.10)

    Returns:
        DataFrame atualizado com valor_isento, valor_tributavel e irrf_10
    """
    df_result = df_dividendos.copy()
    saldo_disponivel = saldo_inicial

    # Data limite para uso do saldo isento
    data_limite = dt.date(2028, 12, 31)

    for idx, row in df_result.iterrows():
        data_pagamento = row['data']
        valor_total = row['valor']

        # Inicializa valores
        valor_isento = 0.0
        valor_tributavel = valor_total
        irrf_10 = 0.0

        # Verifica se a data está dentro do período de validade do saldo
        if data_pagamento <= data_limite and saldo_disponivel > 0:
            # Consome do saldo isento primeiro
            valor_isento = min(valor_total, saldo_disponivel)
            valor_tributavel = valor_total - valor_isento
            saldo_disponivel -= valor_isento

        # Aplica IRRF 10% apenas na parte tributável
        if valor_tributavel > 0:
            # Aqui seria necessário verificar se passa do piso mensal por CNPJ
            # Por simplicidade, vamos assumir que aplica sempre
            irrf_10 = valor_tributavel * irrf_frac

        # Atualiza o DataFrame
        df_result.loc[idx, 'valor_isento'] = valor_isento
        df_result.loc[idx, 'valor_tributavel'] = valor_tributavel
        df_result.loc[idx, 'irrf_10'] = irrf_10

    return df_result, saldo_disponivel

