def fmt_brl(v):
    try: s = float(v)
    except Exception: return "R$ 0,00"
    neg = s < 0; s = abs(s)
    s_str = f"{s:,.2f}".replace(",", "X").replace(".", ",").replace("X",".")
    return f"- R$ {s_str}" if neg else f"R$ {s_str}"

def parse_brl(s):
    if s is None: return 0.0
    if isinstance(s, (int,float)): return float(s)
    s = re.sub(r"[^\d,.-]", "", str(s))
    if "," in s and "." in s: s = s.replace(".","").replace(",",".")
    elif "," in s:            s = s.replace(",",".")
    try: return float(s)
    except Exception: return 0.0

def format_brl_live(value):
    """Formata valor monetário em tempo real conforme o usuário digita"""
    if not value or value == "":
        return ""

    # Remove tudo exceto números
    clean_value = re.sub(r"[^\d]", "", str(value))

    # Se não tem números, retorna vazio
    if not clean_value:
        return ""

    # Remove zeros à esquerda, mas mantém pelo menos um zero
    clean_value = clean_value.lstrip('0') or '0'

    # Se tem apenas 1 dígito, retorna como centavos
    if len(clean_value) == 1:
        return f"0,0{clean_value}"

    # Se tem 2 dígitos, retorna como centavos
    if len(clean_value) == 2:
        return f"0,{clean_value}"

    # Se tem mais de 2 dígitos, formata com separadores de milhares
    # Separa a parte decimal (2 últimos dígitos) da parte inteira
    parte_inteira = clean_value[:-2]
    parte_decimal = clean_value[-2:]

    # Formata a parte inteira com pontos como separadores de milhares
    if len(parte_inteira) > 3:
        # Formatação mais simples: adiciona pontos a cada 3 dígitos
        parte_inteira_formatada = ""
        for i, digit in enumerate(parte_inteira):
            if i > 0 and (len(parte_inteira) - i) % 3 == 0:
                parte_inteira_formatada += "."
            parte_inteira_formatada += digit
    else:
        parte_inteira_formatada = parte_inteira

    return f"{parte_inteira_formatada},{parte_decimal}"

def on_brl_change(change, widget):
    """Callback para formatação em tempo real"""
    try:
        if change['new'] is not None:
            new_value = str(change['new'])
            old_value = str(change.get('old', ''))

            # Se o usuário está digitando (não é uma mudança programática)
            if new_value != old_value:
                # Se o campo está vazio, não faz nada
                if not new_value:
                    return

                # Se já está formatado com R$, não interfere
                if new_value.startswith("R$"):
                    return

                # Se o usuário digitou apenas números, formata em tempo real
                if re.match(r'^[\d,\.]*$', new_value):
                    formatted = format_brl_live(new_value)
                    if formatted and formatted != new_value:
                        # Atualiza sem triggerar o callback novamente
                        widget.unobserve_all()
                        widget.value = formatted
                        widget.observe(lambda change: on_brl_change(change, widget), names='value')

                # Se o usuário digitou algo que parece um valor monetário sem R$
                elif "," in new_value and not new_value.startswith("R$"):
                    # Adiciona R$ se não tem
                    if not new_value.startswith("R$"):
                        widget.unobserve_all()
                        widget.value = f"R$ {new_value}"
                        widget.observe(lambda change: on_brl_change(change, widget), names='value')
    except Exception as e:
        # Em caso de erro, mantém o valor original
        pass


